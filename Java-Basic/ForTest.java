/**
 * Demonstrate the for loop.
 * Call this file "ForTest.java"
 * for(initialization; condition; iteration) statement;
 */
public class ForTest {
    public static void main(String args[]) {
        int x;
        for (x = 0; x < 10; x = x+1) {
            System.out.println("This is x: " + x) ;
        } // end for loop
    } // end main
} // end ForTest